#include "3-calc.h"
/**
 * op_add- function add a + b
 * @a: var a
 * @b: var b
 * Return: int a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub- function sub a - b
 * @a: var a
 * @b: var b
 * Return: int a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul- function add a * b
 * @a: var a
 * @b: var b
 * Return: int a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div- function div a / b
 * @a: var a
 * @b: var b
 * Return: int a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod- function mod a % b
 * @a: var a
 * @b: var b
 * Return: int a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
